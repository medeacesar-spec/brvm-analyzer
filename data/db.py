def get_connection():
    """Retourne une connexion SQLite ou Postgres.
    Interface identique côté appelant (execute, fetchone, commit, close)."""
    if is_postgres():
        try:
            import psycopg
            from psycopg.rows import dict_row
        except ImportError as e:
            raise ImportError(
                "DATABASE_URL pointe vers Postgres mais psycopg n'est pas installé. "
                "Lancer : pip install 'psycopg[binary]'"
            ) from e
        url = _get_database_url()
        conn = psycopg.connect(url, row_factory=dict_row, autocommit=False)
        return _PostgresWrapper(conn)

    # SQLite (défaut)
    conn = sqlite3.connect(DB_PATH)
    conn.row_factory = sqlite3.Row
    conn.execute("PRAGMA journal_mode=WAL")
    return conn


def current_user_id() -> str:
    """Retourne l'identifiant (email) de l'utilisateur connecté.

    - Mode local (pas d'auth) : 'local'
    - Streamlit avec st.user (auth Google OAuth, v1.42+) : renvoie l'email
    - Si une clé 'user_id' est définie dans st.session_state (par ex. lors d'un
      changement de compte), elle est prioritaire.
    """
    try:
        import streamlit as st
        # Priorité : session_state override (utile pour tests/admin)
        override = st.session_state.get("user_id_override") if hasattr(st, "session_state") else None
        if override:
            return override
        user = getattr(st, "user", None)
        if user is not None:
            is_logged = getattr(user, "is_logged_in", False)
            if is_logged:
                email = getattr(user, "email", None)
                if email:
                    return email
    except Exception:
        pass
    return "local"


def read_sql_df(query: str, params=None, parse_dates=None):
    """Exécute une SELECT et renvoie un DataFrame — compatible SQLite + Postgres.
    Remplace `pd.read_sql_query(sql, conn)` qui ne supporte pas nos wrappers Postgres."""
    import pandas as pd
    conn = get_connection()
    try:
        cur = conn.execute(query, params or ())
        rows = cur.fetchall()
        # Récupérer les noms de colonnes
        description = None
        if hasattr(cur, "description") and cur.description:
            description = [d[0] for d in cur.description]
        # Convertir rows en liste de tuples/dict pour pandas
        if rows and isinstance(rows[0], dict):
            df = pd.DataFrame(rows)
        elif description:
            df = pd.DataFrame(rows, columns=description)
        else:
            df = pd.DataFrame(rows)
        # Parse dates
        if parse_dates:
            for col in parse_dates:
                if col in df.columns:
                    df[col] = pd.to_datetime(df[col], errors="coerce")
        return df
    finally:
        conn.close()


def db_info() -> dict:
    """Infos utiles pour debug / UI."""
    db_type = get_db_type()
    info = {"type": db_type}
    if db_type == "sqlite":
        info["path"] = DB_PATH
        info["exists"] = os.path.exists(DB_PATH)
        if info["exists"]:
            info["size_mb"] = round(os.path.getsize(DB_PATH) / 1024 / 1024, 2)
    else:
        url = _get_database_url() or ""
        # Masquer le mot de passe
        import re as _re
        safe = _re.sub(r"://[^:]+:[^@]+@", "://***:***@", url)
        info["url"] = safe
    return info
