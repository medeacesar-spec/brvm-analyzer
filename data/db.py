# Wrapper Postgres (compatible sqlite3.Connection)
# ─────────────────────────────────────────────────────────────────

# Mapping syntaxe SQLite → Postgres
_SQLITE_TO_PG_PATTERNS = [
    # INSERT OR IGNORE → INSERT ... ON CONFLICT DO NOTHING
    (re.compile(r"\bINSERT\s+OR\s+IGNORE\s+INTO\b", re.IGNORECASE), "INSERT INTO"),
    # INSERT OR REPLACE → INSERT ON CONFLICT DO UPDATE (plus complexe, on lève pour être safe)
    # AUTOINCREMENT → GENERATED BY DEFAULT AS IDENTITY
    (re.compile(r"\bINTEGER\s+PRIMARY\s+KEY\s+AUTOINCREMENT\b", re.IGNORECASE),
     "SERIAL PRIMARY KEY"),
    # CURRENT_TIMESTAMP (ok dans les deux) — pas de changement
    # TIMESTAMP DEFAULT CURRENT_TIMESTAMP (compatible)
    # date('now') → CURRENT_DATE
    (re.compile(r"date\('now'\)", re.IGNORECASE), "CURRENT_DATE"),
    (re.compile(r"datetime\('now'\)", re.IGNORECASE), "CURRENT_TIMESTAMP"),
    # substr → SUBSTRING (les deux marchent dans PG mais uniformisons)
    # PRAGMA journal_mode=WAL → no-op sur PG
]


def _translate_query(query: str) -> str:
    """Traduit les motifs SQLite-only vers Postgres."""
    for pattern, replacement in _SQLITE_TO_PG_PATTERNS:
        query = pattern.sub(replacement, query)
    # Placeholder: ? → %s (psycopg utilise %s par défaut)
    # Attention à ne pas toucher les ? dans les strings. Approche naïve :
    # compter les ? hors des quotes.
    query = _replace_placeholders(query)
    # INSERT OR IGNORE : après le SUB ci-dessus, on a "INSERT INTO" — il faut
    # ajouter ON CONFLICT DO NOTHING si c'était un OR IGNORE. On repère par
    # un flag laissé dans le code appelant OU on détecte si le query original
    # contenait OR IGNORE (déjà traité).
    return query


def _replace_placeholders(query: str) -> str:
    """Remplace `?` par `%s` hors des strings littérales."""
    out = []
    in_single = False
    in_double = False
    i = 0
    while i < len(query):
        ch = query[i]
        if ch == "'" and not in_double:
            in_single = not in_single
        elif ch == '"' and not in_single:
            in_double = not in_double
        elif ch == "?" and not in_single and not in_double:
            out.append("%s")
            i += 1
            continue
        out.append(ch)
        i += 1
    return "".join(out)


class _PostgresCursor:
    """Wrapper cursor psycopg → API compatible sqlite3.Cursor.
    Délègue les attributs manquants au cursor sous-jacent (pour pandas, etc.)."""

    def __init__(self, cursor, had_ignore: bool = False):
        self._cur = cursor
        self._had_ignore = had_ignore

    def fetchone(self):
        return self._cur.fetchone()

    def fetchall(self):
        return self._cur.fetchall()

    def fetchmany(self, size=None):
        if size is None:
            return self._cur.fetchmany()
        return self._cur.fetchmany(size)

    def execute(self, query, params=None):
        # Support pour pandas qui appelle cursor.execute() directement
        from data.db import _translate_query, _PostgresWrapper
        translated = _translate_query(query)
        self._cur.execute(translated, params or ())
        return self

    def executemany(self, query, params_list):
        from data.db import _translate_query
        translated = _translate_query(query)
        return self._cur.executemany(translated, params_list)

    def __iter__(self):
        return iter(self._cur)

    @property
    def description(self):
        return self._cur.description

    @property
    def rowcount(self):
        return self._cur.rowcount

    @property
    def lastrowid(self):
        return None

    @property
    def arraysize(self):
        return getattr(self._cur, "arraysize", 1)

    def close(self):
        self._cur.close()

    def __getattr__(self, name):
        # Fallback : déléguer au cursor sous-jacent pour tout attribut manquant
        return getattr(self._cur, name)


class _PostgresWrapper:
    """Wrapper psycopg.Connection → API compatible sqlite3.Connection."""

    def __init__(self, conn):
        self._conn = conn

    def _run(self, query: str, params=None):
        # Traduction SQLite → Postgres
        translated = _translate_query(query)
        had_ignore = "INSERT OR IGNORE" in query.upper()
        if had_ignore:
            if "ON CONFLICT" not in translated.upper():
                translated = translated.rstrip(" ;") + " ON CONFLICT DO NOTHING"

        # PRAGMA table_info(t) → SELECT sur information_schema (émule SQLite)
        use_tuple_row = False
        m = re.match(r"\s*PRAGMA\s+table_info\s*\(\s*(\w+)\s*\)\s*;?\s*$",
                     translated, re.IGNORECASE)
        if m:
            table = m.group(1)
            translated = (
                "SELECT ordinal_position AS cid, column_name AS name, "
                "data_type AS type, "
                "CASE WHEN is_nullable='NO' THEN 1 ELSE 0 END AS notnull, "
                "column_default AS dflt_value, 0 AS pk "
                "FROM information_schema.columns "
                f"WHERE table_name = '{table}' ORDER BY ordinal_position"
            )
            params = None
            use_tuple_row = True  # SQLite PRAGMA renvoie des tuples, pas des dicts

        if use_tuple_row:
            from psycopg.rows import tuple_row
            cur = self._conn.cursor(row_factory=tuple_row)
        else:
            cur = self._conn.cursor()
        cur.execute(translated, params or ())
        return _PostgresCursor(cur, had_ignore=had_ignore)

    def execute(self, query: str, params=None):
        return self._run(query, params)

    def executescript(self, script: str):
        """SQLite's executescript exécute plusieurs statements séparés par `;`.
        En Postgres, on split et exécute chacun."""
        translated = _translate_query(script)
        # Simple split sur `;` — suffisant pour init_db
        for stmt in translated.split(";"):
            stmt = stmt.strip()
            if stmt:
                cur = self._conn.cursor()
                cur.execute(stmt)
                cur.close()

    def commit(self):
        self._conn.commit()

    def rollback(self):
        self._conn.rollback()

    def close(self):
        self._conn.close()

    def cursor(self):
        return _PostgresCursor(self._conn.cursor())


