"""
Couche d'abstraction base de données.

Renvoie une connexion SQLite (local, développement) OU Postgres (cloud, production)
selon la variable DATABASE_URL définie dans :
  1. st.secrets["DATABASE_URL"]  (Streamlit Cloud)
  2. os.environ["DATABASE_URL"]  (shell/CI)
  3. Rien → fallback SQLite sur le fichier local

Cette abstraction permet à tout le code existant (qui utilise `?` placeholder
et `INSERT OR IGNORE`) de continuer à fonctionner localement sans modification.
Pour Postgres, un wrapper léger traduit la syntaxe SQLite → Postgres à la volée.

Usage identique partout :
    from data.db import get_connection
    conn = get_connection()
    conn.execute("SELECT * FROM fundamentals WHERE ticker = ?", ("SNTS.sn",))
"""

import os
import re
import sqlite3
from typing import Optional

from config import DB_PATH


# ─────────────────────────────────────────────────────────────────
# Détection du type de DB
# ─────────────────────────────────────────────────────────────────

def _get_database_url() -> Optional[str]:
    """Lit DATABASE_URL depuis Streamlit secrets, env var, ou None.
    Normalise `postgres://` → `postgresql://` (psycopg n'accepte que la seconde forme)."""
    url = None
    # 1) Streamlit secrets (si dispo et app streamlit en cours)
    try:
        import streamlit as st
        if hasattr(st, "secrets"):
            try:
                url = st.secrets.get("DATABASE_URL")
            except Exception:
                url = None
    except Exception:
        pass
    # 2) Env var
    if not url:
        url = os.environ.get("DATABASE_URL")
    # Normalisation
    if url and url.startswith("postgres://"):
        url = url.replace("postgres://", "postgresql://", 1)
    return url


def get_db_type() -> str:
    """Retourne 'sqlite' ou 'postgres'."""
    url = _get_database_url()
    if url and url.startswith("postgresql://"):
        return "postgres"
    return "sqlite"


def is_postgres() -> bool:
    return get_db_type() == "postgres"


